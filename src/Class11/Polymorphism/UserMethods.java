package Class11.Polymorphism;

public class UserMethods {



    public int addNum(int a, int b) {           // 2
        return a + b;
    }

    public double addNum(double a, int b) {           // 2
        return a + b;
    }

    public double addNum(int a, double b) {           // 2
        return a + b;
    }

    public int addNum(int a, int b, int c) {
        return a + b + c;
    }

    public double addNum(int a, double b, int c) {
        return a + b + c;
    }


}
